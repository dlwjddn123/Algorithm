N = int(input()) # 규칙 찾는게 중요, 이번 문제는 처음 가운데 끝으로 나눠서 풀어야했음
print("*"*N + " "*(N*2-3) + "*"*N) #처음
for i in range(1,N-1):
    print(" "*i + "*" + " "*(N-2) + "*" + " "*((N*2-3)-i*2) + "*" +" "*(N-2) + "*")
print(" "*(N-1) + "*" + " "*(N-2) + "*" + " "*(N-2) + "*") #가운데
for j in range(N-2, 0, -1):
    print(" "*j + "*" + " "*(N-2) + "*" + " "*((N*2-3)-j*2) + "*" +" "*(N-2) + "*")
print("*"*N + " "*(N*2-3) + "*"*N) #끝